    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
         PreparedStatement preparedSt;
         Connection connection = ConnectDatabase.ConnectToDB();
         ResultSet resultSet;
         if(titleField.getText().equals("")){
             JOptionPane.showMessageDialog(null, "title field Can't be blank.");
         }
         else
         {
             try {
            preparedSt=connection.prepareStatement("SELECT * FROM hexadbooklibrary.books where title=?");
            preparedSt.setString(1, titleField.getText());
            resultSet=preparedSt.executeQuery();
            if(resultSet.next())
            {  
                
                bookIdField.setText(resultSet.getString(1));
                authorField.setText(resultSet.getString(3));
                totalCoppiesField.setText(resultSet.getString(4));
               
                
                int cnt=resultSet.getInt(4);
                if(cnt==0){
                
                    JOptionPane.showMessageDialog(null, "The selected book is out of stock.");
                }
                else {
                    JOptionPane.showMessageDialog(null, "Books available: " + cnt);
                }
                
            }
            else{
                JOptionPane.showMessageDialog(this, " Book Not Found");
            }
                
          } catch (SQLException ex) {
            Logger.getLogger(SearchBook.class.getName()).log(Level.SEVERE, null, ex);
            }        
         }
         // TODO add your handling code here:
    }//GEN-LAST:event_searchButtonActionPerformed

