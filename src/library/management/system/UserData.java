    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
    PreparedStatement preparedSt = null;
    Connection connection = ConnectDatabase.ConnectToDB();

    // Validation checks for input fields
    if (userIdField.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter User ID");
        userIdField.requestFocus();
    } else if (nameField.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter User Name");
        nameField.requestFocus();
    } else if (emailField.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter Email");
        emailField.requestFocus();
    } else {
        // Insert data into the database
        try {
            preparedSt = connection.prepareStatement(
                "INSERT INTO `hexadbooklibrary`.`user` (`user_id`, `name`, `email`) VALUES (?, ?, ?)"
            );
            preparedSt.setString(1, userIdField.getText().trim());
            preparedSt.setString(2, nameField.getText().trim());
            preparedSt.setString(3, emailField.getText().trim());

            preparedSt.executeUpdate();
            
            
            JOptionPane.showMessageDialog(rootPane, "User Register", "Registered", JOptionPane.INFORMATION_MESSAGE);

            // Clear fields after successful insertion
            clear();
        } catch (SQLException ex) {
            // Show error message if insertion fails
            JOptionPane.showMessageDialog(rootPane, "Error saving record: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                // Ensure connection is closed after the operation
                if (preparedSt != null) {
                    preparedSt.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(UserData.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
     
    }//GEN-LAST:event_SaveButtonActionPerformed

