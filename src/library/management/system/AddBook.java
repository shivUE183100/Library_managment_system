    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        
        PreparedStatement preparedSt = null;
    Connection connection = ConnectDatabase.ConnectToDB();

    // Validation checks for input fields
    if (bookId.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter Book ID");
        bookId.requestFocus();
    } else if (title.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter Book Ttile ");
        title.requestFocus();
    } else if (author.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter Book Author");
        author.requestFocus();
    } else if (totalCoppies.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter Total coppies");
        totalCoppies.requestFocus();
    } else {
        // Insert data into the database
        try {
            preparedSt = connection.prepareStatement(
                "INSERT INTO `hexadbooklibrary`.`books` (`book_id`, `title`, `author`, `total_copies`) VALUES (?, ?, ?, ?)"
            );
            preparedSt.setString(1, bookId.getText().trim());
            preparedSt.setString(2, title.getText().trim());
            preparedSt.setString(3, author.getText().trim());
            preparedSt.setString(4, totalCoppies.getText().trim()); 
            preparedSt.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Book Added Successfully", "Saved", JOptionPane.INFORMATION_MESSAGE);
            
            // Clear fields after successful insertion
            clear();
        } catch (SQLException ex) {
            // Show error message if insertion fails
            JOptionPane.showMessageDialog(rootPane, "Error saving record: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(AdminLogin.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                // Ensure connection is closed after the operation
                if (preparedSt != null) {
                    preparedSt.close();
                }
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
     
    }//GEN-LAST:event_saveButtonActionPerformed

