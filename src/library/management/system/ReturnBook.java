    private void returnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnButtonActionPerformed
        // TODO add your handling code here:
           if (bookIdField.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter Book Id ");
        bookIdField.requestFocus();
    } else if (userIdField.getText().trim().equals("")) {
        JOptionPane.showMessageDialog(rootPane, "Please enter User Id");
        userIdField.requestFocus();
    } else {
        
        String userIdText = userIdField.getText(); // Get the text from the userIdField;
            int userId= Integer.parseInt(userIdText);
            
            String bookIdText = bookIdField.getText(); // Get the text from the bookIdField;
            int bookId= Integer.parseInt(bookIdText);
            
            String returnBookQuery = "UPDATE BorrowedBooks SET returned_at = CURRENT_TIMESTAMP WHERE user_id = ? AND book_id = ? AND returned_at IS NULL";
            String updateBookCount = "UPDATE books SET total_copies = total_copies + 1 WHERE book_id = ?";
            String DeleteReturnBook ="DELETE FROM borrowedbooks WHERE book_id = ?";
        try (Connection connection = ConnectDatabase.ConnectToDB();
             PreparedStatement returnBookSt = connection.prepareStatement(returnBookQuery);
             PreparedStatement updateCountSt = connection.prepareStatement(updateBookCount);
             PreparedStatement deleteBookSt = connection.prepareStatement(DeleteReturnBook)) {

            returnBookSt.setInt(1, userId);
            returnBookSt.setInt(2, bookId);
            int rowsAffected = returnBookSt.executeUpdate();

            if (rowsAffected > 0) {
                updateCountSt.setInt(1, bookId);
                updateCountSt.executeUpdate();
                deleteBookSt.setInt(1, bookId);
                deleteBookSt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Book returned successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "No borrowed book found for this user and book.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);

        }
    }
            
           
            
    }//GEN-LAST:event_returnButtonActionPerformed

